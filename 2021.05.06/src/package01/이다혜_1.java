package package01;

public class 이다혜_1 {
	
	public void gugu(int starDan, int endDan, int starGop, int endGop) {
		for(int dan = starDan; dan <= endDan; dan++) {
			for(int gop = starGop; gop <= endGop; gop++) {
				System.out.println(dan + " * " + gop + " = " + dan * gop);
				
			}
		}
		
	}
	
	

}

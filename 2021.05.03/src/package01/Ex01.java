package package01;

public class Ex01 {
	
	public void gugu (int startDan, int endDans, int startGop, int endGop) {
		for(int dan = startDan; dan <= endDans; dan++) {
			for(int gop = startGop; gop <= endGop; gop ++) {
				System.out.println(dan+"*"+gop + " = " + dan*gop);
			}
		}
	}
	
	
	
}


